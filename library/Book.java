public class Book {
	private String id;
	private String title;
	
//	encapsulation
//	  set id
	  public void setId(String id) {
		  this.id = id;
	  }
//	  get id
	  public String getId() {
		  return id;
	  }
	  
//	  set name
	  public void setTitle(String title) {
		  this.title = title;
	  }
//	  get name
	  public String getTitle() {
		  return title;
	  }
	
}
